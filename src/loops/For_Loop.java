package loops;

public class For_Loop {

    public static void main(String[] args) {

        for (int i=0; i<5; i++){
            System.out.println("hello "+i);
        }

        for (int i=0; i<5; i++){
            System.out.println("Namaskar "+i);
        }
        for (int i=5; i>0; i--){
            System.out.println("hello "+i);
        }

        for(int i=10; i>0; i--){
            System.out.println("namaskar "+i);
        }

        for (int i=1; i<=10; i++){
            System.out.println("counting "+i);
        }

        for (int i=1; i<=5; i++){
            System.out.println("India "+i);
        }

        for (int i=5; i>0; i--){
            System.out.println("America "+i);
        }
    }
}
