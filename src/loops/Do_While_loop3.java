package loops;

public class Do_While_loop3 {
    public static void main(String[] args) {

        int i=0;
        do {
            System.out.println("hello "+i);
            i++;
        }while (i<=5);

        int j=0;
        do {
            System.out.println("Asus "+j);
            j++;
        }while (j<=5);

        int h=10;
        do {
            System.out.println("home "+h);
            h--;
        }while (h>=6);

        int q=45;
        do {
            System.out.println("alt "+q);
            q--;
        }while (q>40);

        int p=15;
        do {
            System.out.println("polo "+p);
            p++;
        }while (p<=20);

        int e=85;
        do {
            System.out.println("Earn "+e);
            e++;
        }while (e<=90);

        int age=24;
        do {
            System.out.println("Older "+age);
            age++;
        }while (age<30);

        int w=85;
        do {
            System.out.println("man in blue "+w);
            w++;
        }while (w<90);

        int r=41;
        do {
            System.out.println("Australia "+r);
            r--;
        }while (r>35);

        int x=23;
        do {
            System.out.println("China "+x);
            x--;
        }while (x>20);

        int I=39;
        do {
            System.out.println("Radha "+I);
            I++;
        }while (I<=43);
    }

}
