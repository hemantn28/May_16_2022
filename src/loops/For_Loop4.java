package loops;

public class For_Loop4 {

    public static void main(String[] args) {

        for (int i=0; i<5; i++){
            System.out.println("hello "+i);
        }

        for (int i=10; i<20; i++){
            System.out.println("india "+i);
        }

        for (int i=5; i>0; i--){
            System.out.println("Ram "+i);
        }

        for (int i=65; i>60; i--){
            System.out.println("Krishna "+i);
        }
        for (int i=658; i>654; i--){
            System.out.println("Shift "+i);
        }

        for (int that=25; that<30; that++){
            System.out.println("than "+that);
        }

        for (int me=21; me>18; me--){
            System.out.println("I "+me);
        }
    }
}
