package loops;

public class While_Loop {
    public static void main(String[] args) {

        int i=0;
        while (i<5){
            System.out.println("Hello "+i);
            i++;
        }
        int n=0;
        while (n<10){
            System.out.println("while "+n);
            n++;
        }

        int m=0;
        while (m<=10){
            System.out.println("India "+m);
            m++;
        }

        int k=10;
        while (k>0){
            System.out.println("Amritsar "+k);
            k--;
        }

        int u=20;
        while (u>0){
            System.out.println("Rao "+u);
            u--;
        }

        int t=8;
        while (t>1){
            System.out.println("Kapadia "+t);
            t--;
        }

        int g=0;
        while (g<=11){
            System.out.println("Ram "+g);
            g++;
        }

        int p=0;
        while (p<11){
            System.out.println("Hello "+p);
            p++;
        }

        int r=10;
        while (r>0){
            System.out.println("Aakash "+r);
            r--;
        }

        int yu=6;
        while (yu>0){
            System.out.println("Stone "+yu);
            yu--;
        }

        int a=0;
        while (a<11){
            System.out.println("age "+a);
            a++;
        }

        int b=10;
        while (b>0){
            System.out.println("yahoo "+b);
            b--;
        }
    }
}
