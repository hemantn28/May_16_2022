package loops;

public class Do_While_Loop {

    public static void main(String[] args) {
        int i=0;
        do {
            System.out.println("Hello "+i);
            i++;
        }while (i<5);

        int j=0;
        do {
            System.out.println("India "+j);
            j++;
        }while (j<10);

        int k=0;
        do {
            System.out.println("System "+k);
            k++;
        }while (k<=7);

        int h=0;
        do {
            System.out.println("Fighter "+h);
            h++;
        }while (h<8);

        int mi=10;
        do {
            System.out.println("while "+mi);
            mi--;
        }while (mi>0);

        int as=20;
        do {
            System.out.println("Vrindavan "+as);
            as--;
        }while (as>0);

        int q=0;
        do {
            System.out.println("Hello "+q);
            q++;
        }while (q<5);
    }
}
