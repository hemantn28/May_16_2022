package loops;

public class Tables {

    public static void main(String[] args) {

        int n=2;
        for (int i=1; i<=10; i++){
            int result=n*i;
            System.out.println(result);
        }

        int m=3;
        for (int i=1; i<11; i++){
            int result=m*i;
            System.out.println(result);
        }
        int k=6;
        for (int i=1; i<11; i++){
            int result=k*i;
            System.out.println(result);
        }

        int y=17;
        for (int i=1; i<=10; i++){
            int result=y*i;
            System.out.println("Table Of 17= "+result);
        }

        int ni=19;
        for (int i=1; i<=10; i++){
            int result=ni*i;
            System.out.println(result);
        }

    }
}
