package loops;

public class While_Loop2 {
    public static void main(String[] args) {

        int i=0;
        while (i<5){
            System.out.println("hello "+i);
            i++;
        }

        int j=0;
        while (j<=5){
            System.out.println("india "+j);
            j++;
        }

        int k=0;
        while (k<6){
            System.out.println("m "+k);
            k++;
        }

        int ii=1;
        while (ii<=7){
            System.out.println("kite "+ii);
            ii++;
        }



    }
}
