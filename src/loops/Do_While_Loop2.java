package loops;

public class Do_While_Loop2 {

    public static void main(String[] args) {

        int i=0;
        do {
            System.out.println("hello "+i);
            i++;
        }while (i<5);

        int j=1;
        do {
            System.out.println("India "+j);
            j++;
        }while (j<=10);

        int k=5;
        do {
            System.out.println("Aligarh "+k);
            k++;
        }while (k<=10);

        int z=54;
        do {
            System.out.println("Agra "+z);
            z--;
        }while (z>=50);

        int f=87;
        do {
            System.out.println("Etah "+f);
            f--;
        }while (f>80);

        int n=97;
        do {
            System.out.println("Tundla "+n);
            n--;
        }while (n>=95);

        int x=41;
        do {
            System.out.println("Java "+x);
            x--;
        }while (x>36);

        int s=31;
        do {
            System.out.println("Ram "+s);
            s++;
        }while (s<37);

        int v=77;
        do {
            System.out.println("Search "+v);
            v++;
        }while (v<81);

        int a=10;
        do {
            System.out.println("loop "+a);
            a++;
        }while (a<5);

        //Q. Write a programme to print first n natural numbers using do-while loop.

        int ab=1;
        do {
            System.out.println("Natural Numbers 1 to 10 = "+ab);
            ab++;
        }while (ab<=10);

    }
}
