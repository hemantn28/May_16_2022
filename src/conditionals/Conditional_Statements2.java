package conditionals;

public class Conditional_Statements2 {
    public static void main(String[] args) {
        int age=19;
        if (age>18){
            System.out.println("you are eligible for voting");
        }
        else{
            System.out.println("you are not eligible for voting");
        }

        int marks=80;
        if (marks>75){
            System.out.println("you got distinction");
        }
        else{
            System.out.println("you did not get distinction");
        }

        float distance=18.5f;
        if (distance>15){
            System.out.println("your home is far from here");
        }
        else{
            System.out.println("your home is not so far from here");
        }

        String city="Bangalore";
        if (city=="Bangalore"){
            System.out.println("You are in the Bangalore");
        }
        else {
            System.out.println("You are not in the Bangalore");
        }

        String name="Krishna";
        if (name=="Ram"){
            System.out.println("You are Ram");
        }
        else{
            System.out.println("You are not Ram");
        }

    }
}
